        if(radEmpleado.isSelected()){
            asignacion.setEmpleado(empleadoSeleccionado);
        }else if(radAlumnos.isSelected()){
            
        }else{
            GrupoHorario grupo = (GrupoHorario)this.cboGrupoHorario.getSelectedItem();            
            asignacion.setGrupoHorario(grupo);
        }
