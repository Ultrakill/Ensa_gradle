    private void btnExportarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportarActionPerformed
        // TODO add your handling code here:
        Calendar cal = Calendar.getInstance();
        cal.setTime(empleado.getContratoList().get(0).getFechaInicio());
        int numPeriodo = Integer.parseInt(datos[2].substring(0, 4));
        cal.set(Calendar.YEAR, numPeriodo);
        Date inicio = cal.getTime();
        cal.set(Calendar.YEAR, numPeriodo + 1);
        Date fin = cal.getTime();

        if (this.tblTabla.getRowCount() == 0) {
            JOptionPane.showMessageDialog(null, "No hay datos en la tabla para exportar.", "BCO",
                    JOptionPane.INFORMATION_MESSAGE);
            this.btnExportar.grabFocus();
            return;
        }
        JFileChooser chooser = new JFileChooser();

        FileNameExtensionFilter filter = new FileNameExtensionFilter("Archivos de excel", "xls");
        chooser.setFileFilter(filter);
        chooser.setDialogTitle("Guardar archivo");
        chooser.setMultiSelectionEnabled(false);
        chooser.setAcceptAllFileFilterUsed(false);

        if (chooser.showSaveDialog(null) == JFileChooser.APPROVE_OPTION) {
            List<JTable> tb = new ArrayList<>();
            List<String> nom = new ArrayList<>();
            tb.add(tblTabla);
            nom.add("Periodos vacacionales");
            String excel = chooser.getSelectedFile().toString().concat(".xls");
            try {
//                Date[] fechasLimite = this.obtenerFechasLimite();
                Exportador e = new Exportador(new File(excel), tb, nom, inicio, fin, false);
                if (e.exportar()) {
                    JOptionPane.showMessageDialog(null, "Los datos fueron exportados a excel.", "EXPORTAR EXCEL",
                            JOptionPane.INFORMATION_MESSAGE);

                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Hubo un error" + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnExportarActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int fila = tblTabla.getSelectedRow();
        if (fila != -1) {
            Vacacion vacacion = this.listadoVacaciones.get(fila);
            if (!vacacion.isHayReprogramacion()) {
                DlgInterrupcionVacacion interrupcion = new DlgInterrupcionVacacion(this, vacacion);
                interrupcion.setVisible(true);

                listadoVacacionesInterrumpidas.clear();
                listadoVacacionesInterrumpidas = ivc.buscarXVacacion(vacacion);
                tblTablaInterrupciones.packAll();

                listadoVacaciones.clear();
                
                System.out.println("DATOS A BUSCAR EN DETALLE: "+empleadoBusqueda.getNombreCompleto()+ " - "+periodoBusqueda.getAnio());
                listadoVacaciones.addAll(vc.buscarXEmpleadoXPeriodo(empleadoBusqueda, periodoBusqueda));
                tblTabla.packAll();

//                List<Vacacion> lista = vc.contarXEmpleadoXFecha(empleado, fechaInicio, fechaFin);
//                listadoVacaciones.addAll(lista);
//                tblTabla.packAll();
            } else {
                JOptionPane.showMessageDialog(null, "El registro de vacacion seleccionado ha sido reprogramado", "Mensaje del Sistema", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

