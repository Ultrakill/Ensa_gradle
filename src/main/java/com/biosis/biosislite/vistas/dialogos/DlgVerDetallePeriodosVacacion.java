        if (evt.getClickCount() == 1) {
            int fila = tblTabla.getSelectedRow();
            if (fila != -1) {
                listadoVacacionesInterrumpidas.clear();
                listadoVacacionesReprogramadas.clear();

                Vacacion filaSeleccionada = listadoVacaciones.get(fila);

                if (filaSeleccionada.isHayInterrupcion()) {
                    listadoVacacionesInterrumpidas = ivc.buscarXVacacion(filaSeleccionada);

                    MTInterrupcionVacacion mtiv = new MTInterrupcionVacacion(listadoVacacionesInterrumpidas);
                    tblTablaInterrupciones.setModel(mtiv);
                    tblTablaInterrupciones.packAll();
                }
                if (filaSeleccionada.isHayReprogramacion()) {
                    listadoVacacionesReprogramadas = vc.buscarReprogramacion(filaSeleccionada);

                    MTVacacionReprogramada mtav = new MTVacacionReprogramada(listadoVacacionesReprogramadas);
                    tblTablaReprogramaciones.setModel(mtav);
                    tblTablaReprogramaciones.packAll();

                }

            }

        }
