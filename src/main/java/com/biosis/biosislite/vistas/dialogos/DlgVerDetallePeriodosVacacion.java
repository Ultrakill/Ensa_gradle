    private void llenarDatos() {
        listadoVacacionesReprogramadas = new ArrayList();
        listadoVacacionesReprogramadas = ObservableCollections.observableList(listadoVacacionesReprogramadas);

        
        int numPeriodo = Integer.parseInt(datos[2].substring(0, 4));
        Periodo periodoObtenido = pc.buscarPeriodoxAnio(numPeriodo).get(0);
        Empleado empleadoObtenido = ec.buscarPorCodigoSAP(datos[0]);
        empleado = empleadoObtenido;
        empleadoBusqueda = empleadoObtenido;
        periodoBusqueda = periodoObtenido;
        
        listadoVacacionesInterrumpidas = ivc.buscarInterrupcionXEmpleadoXPeriodo(empleadoBusqueda, periodoBusqueda);
        listadoVacacionesInterrumpidas = ObservableCollections.observableList(listadoVacacionesInterrumpidas);


        llenarLabels();

        listadoVacaciones = vc.buscarXEmpleadoXPeriodo(empleadoObtenido, periodoObtenido);
        listadoVacaciones = ObservableCollections.observableList(listadoVacaciones);

        MTAsignarVacacion mt = new MTAsignarVacacion(listadoVacaciones);

        tblTabla.setModel(mt);
        tblTabla.packAll();

    }

